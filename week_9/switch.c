#include <stdio.h>

int main(void)
{
    char letter = 'a';
    if (letter == 'a')
    {
        printf("apple\n");
    }
    else if (letter == 'b')
    {
        printf("banana\n");
    }
    else if (letter == 'c')
    {
        printf("cat\n");
    }
    else if (letter == 'd')
    {
        printf("other\n");
    }

    return 0;
}
